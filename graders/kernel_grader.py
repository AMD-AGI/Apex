def find_tasks(output_dir: Path) -> list[Path]:
    """Return task directories that contain a solution file."""
    tasks = []
    for d in sorted(output_dir.iterdir()):
        if not d.is_dir():
            continue
        if any((d / s).exists() for s in SOLUTION_NAMES):
            tasks.append(d)
    return tasks


def find_solution(task_dir: Path) -> Path | None:
    for name in SOLUTION_NAMES:
        p = task_dir / name
        if p.exists():
            return p
    return None


def _detect_benchmark_tampering(solution_path: Path, baseline_path: str) -> list[str]:
    """Detect solutions that tamper with the benchmark via __main__ fast-exit tricks.

    Only flags ``__main__`` blocks that contain genuinely suspicious patterns
    (SystemExit, hardcoded PASS, fabricated timings). A ``__main__`` block
    that simply runs the kernel for testing is NOT flagged.

    Returns a list of warning strings. An empty list means no tampering detected.
    """
    warnings: list[str] = []
    try:
        src = solution_path.read_text()
        tree = ast.parse(src)
    except Exception:
        return warnings

    for node in ast.walk(tree):
        if not isinstance(node, ast.If):
            continue
        test = node.test
        is_main_guard = False
        if isinstance(test, ast.Compare):
            left = test.left
            if isinstance(left, ast.Name) and left.id == "__name__":
                is_main_guard = True
            elif isinstance(left, ast.Constant) and left.value == "__main__":
                is_main_guard = True
        if not is_main_guard:
            continue

        body_src = ast.get_source_segment(src, node)
        if body_src is None:
            continue

        if "SystemExit" in body_src or "sys.exit" in body_src or "os._exit" in body_src:
            warnings.append(
                "Solution has SystemExit/sys.exit inside __main__ guard — "
                "likely tampering with standalone benchmark measurement"
            )
        if re.search(r'BENCHMARK_MS:\s*[\d.]+', body_src):
            warnings.append(
                "Solution prints hardcoded BENCHMARK_MS inside __main__ guard — "
                "fabricated benchmark number"
            )
        if re.search(r'print\s*\(\s*["\']PASS["\']\s*\)', body_src):
            warnings.append(
                "Solution prints hardcoded PASS inside __main__ guard — "
                "bypassing correctness check"
            )

    return warnings


