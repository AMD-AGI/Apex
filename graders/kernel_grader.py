def _try_magpie_perf_measurement(
    raw: dict,
    baseline_path: str,
    optimized_path: str,
    task_dir: Path,
    compare_timeout: int,
    solution: Path,
    testcase_cmd: str | None = None,
) -> None:
    """Attempt Magpie compare for performance measurement only.

    Called after library_test or accordo has already determined correctness.
    If Magpie provides timing data, injects ``_magpie_speedup`` into *raw*
    so that ``_finalize_grading_result`` uses Magpie numbers instead of the
    less-precise script-level benchmark.  The domain-specific correctness
    verdict is never overridden.

    *testcase_cmd* tells Magpie how to invoke the kernel for benchmarking.
    Without it Magpie may skip the performance phase entirely.
    """
    if not baseline_path or not Path(baseline_path).exists():
        return
    if not optimized_path or not Path(optimized_path).exists():
        return

    kernel_type = _detect_kernel_type(solution)
    try:
        magpie_raw = run_magpie_compare(
            baseline_path=baseline_path,
            optimized_path=optimized_path,
            testcase_cmd=testcase_cmd,
            kernel_type=kernel_type,
            working_dir=str(task_dir.resolve()),
            timeout=compare_timeout,
        )
        if "error" not in magpie_raw:
            _, _, speedup = parse_compare_result(magpie_raw)
            if speedup > 0:
                raw["_magpie_speedup"] = speedup
                raw["_magpie_perf_source"] = "magpie_compare"
                print(f"    [grader] Magpie perf measurement: {speedup:.3f}x",
                      file=sys.stderr)
            else:
                print("    [grader] Magpie perf: no timing data returned",
                      file=sys.stderr)
        else:
            print(f"    [grader] Magpie perf skipped: {magpie_raw['error']}",
                  file=sys.stderr)
    except Exception as exc:
        print(f"    [grader] Magpie perf failed: {exc}", file=sys.stderr)


def grade_task(
    task_dir: Path,
    isolate_caches: bool = True,
    gpu_device: int = 0,
    trust_agent_config: bool = False,
    speedup_cap: float = 0.0,
    compute_rl_reward: bool = False,
    require_tags: bool = True,
    kernel_backend: str | None = None,
) -> KernelResult:
    """Grade a single task directory.

    Reads config.yaml, finds baseline + solution, runs Magpie compare.
    Falls back to script-level benchmarking when Magpie doesn't report timing.

    By default (trust_agent_config=False), any agent-authored config.yaml
    is validated and regenerated by the pipeline to prevent the agent from
    writing trivial tests or fabricating benchmark numbers.

    Parameters
    ----------
    compute_rl_reward : bool
        If True, also compute and attach the gated RL reward in
        ``result.raw["rl_reward"]``. Default False (legacy scoring only).
    require_tags : bool
        Whether ``<answer>`` tags are required for the gated reward.
    kernel_backend : str | None
        Override for the kernel backend (``"triton"`` or ``"hip"``).
        Auto-detected from solution file extension if None.
    """
    task_id  = task_dir.name
    solution = find_solution(task_dir)
    config   = task_dir / "config.yaml"

    if solution is None:
        return KernelResult(task_id=task_id, error="no solution file found")

    if not trust_agent_config and validate_config is not None:
        validation = validate_config(task_dir)
        for w in validation.warnings:
            print(f"    [config] {w}", file=sys.stderr)
        for e in validation.errors:
            print(f"    [config] ERROR: {e}", file=sys.stderr)
        if not validation.valid:
            if write_config is not None:
                try:
                    write_config(task_dir, gpu_device=gpu_device)
                    print(f"    [config] Regenerated trusted config.yaml", file=sys.stderr)
                except Exception as exc:
                    if not config.exists():
                        return KernelResult(
                            task_id=task_id,
                            error=f"Cannot generate config.yaml: {exc}",
                        )
    elif not config.exists():
        return KernelResult(
            task_id=task_id,
            compiled=True,
            error=f"config.yaml missing in {task_dir}; solution exists but cannot run Magpie without config",
        )

    clear_pycache(solution)

    if isolate_caches:
        with isolated_grading_env(gpu_device=gpu_device):
            result = _grade_task_inner(task_dir, task_id, solution, config, speedup_cap)
    else:
        result = _grade_task_inner(task_dir, task_id, solution, config, speedup_cap)

    if compute_rl_reward:
        _attach_gated_reward(result, task_dir, require_tags, kernel_backend)

    return result


def _attach_gated_reward(
    result: KernelResult,
    task_dir: Path,
    require_tags: bool,
    kernel_backend: str | None,
) -> None:
    """Compute and attach the RL gated reward to a KernelResult.

    Stores the reward in ``result.raw["rl_reward"]`` without affecting
    ``result.score`` (the legacy leaderboard score).
    """
    try:
        from score import compute_reward_gated
    except ImportError:
        result.raw["rl_reward"] = None
        return

    solution = find_solution(task_dir)
    if solution is None:
        result.raw["rl_reward"] = None
        return

    try:
        solution_str = solution.read_text(encoding="utf-8", errors="replace")
    except OSError:
        result.raw["rl_reward"] = None
        return

    # Determine backend from file extension if not provided
    if kernel_backend is None:
        kernel_backend = "hip" if solution.suffix in (".hip", ".cu") else "triton"

    # Extract timing from raw results
    raw = result.raw
    baseline_ms = float(raw.get("baseline_ms", 0) or 0)
    optimized_ms = float(raw.get("optimized_ms", 0) or 0)
    # Synthesize from speedup ratio when absolute times aren't available
    if baseline_ms <= 0 and optimized_ms <= 0 and result.speedup > 0:
        optimized_ms = 1.0
        baseline_ms = result.speedup * optimized_ms

    correctness_score = 1.0 if result.correct else 0.0
    hacking_detected = bool(raw.get("_tampering_warnings") or raw.get("_gaming_warnings"))
    timed_out = "timed out" in (result.error or "").lower()

    magpie_result = {
        "compiled": result.compiled,
        "correctness_score": correctness_score,
        "baseline_ms": baseline_ms,
        "optimized_ms": optimized_ms,
        "hacking_detected": hacking_detected,
        "timed_out": timed_out,
    }

    # Build ground truth from config metadata
    ground_truth: dict = {}
    cfg_path = task_dir / "config.yaml"
    if cfg_path.exists():
        try:
            cfg = parse_task_config(cfg_path)
            meta = cfg.get("_pipeline_metadata", {})
            if meta.get("allowed_imports"):
                ground_truth["allowed_imports"] = meta["allowed_imports"]
            if meta.get("op_type"):
                ground_truth["op_type"] = meta["op_type"]
        except Exception:
            pass

    trace_context = {"task_id": result.task_id}

    # Compute gated reward using individual components directly.
    # The full compute_gated_reward() AST gate is designed for RL training
    # with self-contained kernels. Apex pipeline kernels are real library code
    # (wrappers importing from sub-modules), so we skip the AST gate and use
    # the existing _detect_benchmark_tampering for cheat detection instead.
    try:
        from reward_fn import (
            R_FORMAT_PASS, R_COMPILE_PASS, R_CORRECT_WEIGHT,
            compute_performance_reward,
        )
        r_format = R_FORMAT_PASS  # bare .py files always get format pass
        r_compile = R_COMPILE_PASS if result.compiled else 0.0
        r_correct = R_CORRECT_WEIGHT * correctness_score
        r_perf = compute_performance_reward(
            baseline_ms, optimized_ms, correctness_score,
        )
        # Apply tampering penalty from existing anti-cheat system
        r_ast = -2.0 if hacking_detected else 0.0
        reward = r_format + r_ast + r_compile + r_correct + r_perf
        result.raw["rl_reward"] = round(reward, 4)
        result.raw["rl_reward_breakdown"] = {
            "r_format": r_format, "r_ast": r_ast,
            "r_compile": r_compile, "r_correct": round(r_correct, 4),
            "r_perf": round(r_perf, 4),
        }
    except Exception as exc:
        print(f"    [grader] gated reward failed: {exc}", file=sys.stderr)
        result.raw["rl_reward"] = None


def _grade_task_inner(
    task_dir: Path,
    task_id: str,
    solution: Path,
    config: Path,
    speedup_cap: float = 0.0,
) -> KernelResult:
    """Core grading logic (runs inside or outside cache isolation).

    Dispatches to the appropriate correctness mode based on config.yaml:
      - pytorch (default): magpie compare with testcase_cmd
      - library_test: run library's own test suite
      - accordo: HSA-level binary comparison via Accordo CLI
    """
    cfg = _parse_config(config)

    baseline_path = cfg.get("baseline", {}).get("path", "")

    corr_cfg = cfg.get("correctness", {})
    corr_mode = corr_cfg.get("mode", "pytorch")

    meta = cfg.get("_pipeline_metadata", {})
    framework = meta.get("framework", "")
    has_heavy_imports = framework in ("vllm", "sglang")
    compare_timeout = 600 if has_heavy_imports else 300

    # Resolve baseline path (shared across all modes that need it)
    if baseline_path:
        if not Path(baseline_path).is_absolute():
            baseline_path = str((task_dir / baseline_path).resolve())

        if not Path(baseline_path).exists():
            for fallback_name in ("baseline_ref.py", "baseline.py", "baseline_ref.hip", "baseline.hip"):
                fallback = task_dir / fallback_name
                if fallback.exists():
                    baseline_path = str(fallback.resolve())
                    print(f"    [grader] Configured baseline missing, using {fallback_name}", file=sys.stderr)
                    break
            else:
                rocm_root = REPO_ROOT / "tools" / "rocm"
                raw_bp = cfg.get("baseline", {}).get("path", "")
                for prefix in ("", "vllm/", "aiter/"):
                    candidate = rocm_root / prefix / raw_bp
                    if candidate.exists():
                        baseline_path = str(candidate.resolve())
                        print(f"    [grader] Resolved baseline via tools/rocm/{prefix}", file=sys.stderr)
                        break

    optimized_path = cfg.get("optimized", {}).get("path", str(solution))
    if not Path(optimized_path).is_absolute():
        optimized_path = str((task_dir / optimized_path).resolve())

    # Testcase command for Magpie perf (correctness or performance section)
    magpie_testcase_cmd = (
        corr_cfg.get("command")
        or cfg.get("performance", {}).get("command")
        or None
    )

    # ── Mode 2: Library test ─────────────────────────────────────────────
    if corr_mode == "library_test":
        unit_test_cmd = corr_cfg.get("unit_test_command", "")
        lib_working_dir = corr_cfg.get("working_directory", str(task_dir.resolve()))

        if not unit_test_cmd:
            return KernelResult(
                task_id=task_id,
                error="library_test mode but no unit_test_command in config",
            )

        raw = _run_library_test(
            unit_test_cmd=unit_test_cmd,
            working_dir=lib_working_dir,
            solution_path=solution,
            timeout=compare_timeout,
        )

        if raw.get("correct") and baseline_path:
            _try_magpie_perf_measurement(
                raw, baseline_path, optimized_path,
                task_dir, compare_timeout, solution,
                testcase_cmd=magpie_testcase_cmd,
            )

        return _finalize_grading_result(
            task_id, raw, cfg, task_dir,
            baseline_path, optimized_path, solution, compare_timeout,
            speedup_cap=speedup_cap,
        )

    # ── Mode 3: Accordo HSA-level ────────────────────────────────────────
    if corr_mode == "accordo":
        accordo_inner = corr_cfg.get("accordo", {})
        acc_working_dir = corr_cfg.get("working_directory", str(task_dir.resolve()))

        raw = _run_accordo_check(
            accordo_cfg=accordo_inner,
            working_dir=acc_working_dir,
            timeout=compare_timeout,
        )

        if raw.get("correct") and baseline_path:
            _try_magpie_perf_measurement(
                raw, baseline_path, optimized_path,
                task_dir, compare_timeout, solution,
                testcase_cmd=magpie_testcase_cmd,
            )

        return _finalize_grading_result(
            task_id, raw, cfg, task_dir,
            baseline_path, optimized_path, solution, compare_timeout,
            speedup_cap=speedup_cap,
        )

    # ── Mode 1: PyTorch / Magpie compare (default) ───────────────────────
    if not baseline_path:
        return KernelResult(
            task_id=task_id,
            error="config.yaml missing baseline.path",
        )

    # Verify pipeline-generated correctness files haven't been tampered with
    protected_hashes = meta.get("protected_file_hashes", {})
    for fname, expected_hash in protected_hashes.items():
        fpath = task_dir / fname
        if not fpath.exists():
            return KernelResult(
                task_id=task_id,
                error=f"tamper check: pipeline file {fname} was deleted by agent",
            )
        actual_hash = hashlib.sha256(fpath.read_bytes()).hexdigest()
        if actual_hash != expected_hash:
            print(f"    [grader] TAMPER DETECTED: {fname} was modified by agent", file=sys.stderr)
            return KernelResult(
                task_id=task_id,
                error=f"tamper check: {fname} was modified (expected {expected_hash[:12]}..., got {actual_hash[:12]}...)",
            )

    testcase_cmd = corr_cfg.get("command")
    kernel_type = _detect_kernel_type(solution)

    raw = run_magpie_compare(
        baseline_path=baseline_path,
        optimized_path=optimized_path,
        testcase_cmd=testcase_cmd,
        kernel_type=kernel_type,
        working_dir=str(task_dir.resolve()),
        timeout=compare_timeout,
    )

    if "error" in raw:
        return KernelResult(task_id=task_id, raw=raw, error=raw["error"])

    compiled, correct, speedup = parse_compare_result(raw)

    # Magpie may not report timing; fall back to script-level benchmarking
    if compiled and correct and speedup <= 0:
        perf_cfg = cfg.get("performance", {})
        perf_cmd = perf_cfg.get("command", "")
        python_bin = perf_cmd.split()[0] if perf_cmd else "python3"

        b_ms = _run_benchmark_script(baseline_path, python_bin, str(task_dir))
        o_ms = _run_benchmark_script(optimized_path, python_bin, str(task_dir))
        if b_ms > 0 and o_ms > 0:
            speedup = b_ms / o_ms
            if speedup > 100:
                speedup = 0.0
            raw["baseline_ms"] = round(b_ms, 4)
            raw["optimized_ms"] = round(o_ms, 4)
            raw["_benchmark_speedup"] = round(speedup, 4)

    # Store Magpie results into raw so _finalize_grading_result can use them
    raw["compiled"] = compiled
    raw["correct"] = correct
    if speedup > 0:
        raw["_magpie_speedup"] = speedup

    return _finalize_grading_result(
        task_id, raw, cfg, task_dir,
        baseline_path, optimized_path, solution, compare_timeout,
        speedup_cap=speedup_cap,
    )


def grade_all(
    output_dir: Path,
    task_filter: str | None = None,
    isolate_caches: bool = True,
    gpu_device: int = 0,
    trust_agent_config: bool = False,
) -> list[KernelResult]:
    if not output_dir.exists():
        print(f"[kernel_grader] output dir not found: {output_dir}", file=sys.stderr)
        return []

    tasks = find_tasks(output_dir)
    if task_filter:
        tasks = [t for t in tasks if task_filter in t.name]

    if not tasks:
        print(f"[kernel_grader] no tasks found in {output_dir}", file=sys.stderr)
        return []

    results = []
    for task_dir in tasks:
        print(f"  grading {task_dir.name} ...", file=sys.stderr)
        r = grade_task(
            task_dir,
            isolate_caches=isolate_caches,
            gpu_device=gpu_device,
            trust_agent_config=trust_agent_config,
        )
        results.append(r)
        status = "✓" if r.correct else ("?" if r.compiled else "✗")
        print(
            f"    [{status}] compiled={r.compiled} correct={r.correct} "
            f"speedup={r.speedup:.2f}× score={r.score:.0f}",
            file=sys.stderr,
        )

    return results


def summarise(results: list[KernelResult]) -> dict:
    if not results:
        return {"total_score": 0, "tasks": 0, "results": []}

    total     = sum(r.score    for r in results)
    compiled  = sum(r.compiled for r in results)
    correct   = sum(r.correct  for r in results)
    avg_sp    = sum(r.speedup  for r in results if r.correct) / max(1, correct)

    return {
        "total_score":   round(total, 2),
        "tasks":         len(results),
        "compiled":      compiled,
        "correct":       correct,
        "avg_speedup":   round(avg_sp, 4),
        "scoring_notes": {
            "compiled":  f"+{PTS_COMPILED} pts",
            "correct":   f"+{PTS_CORRECT} pts",
            "speedup":   "+speedup×100 pts",
        },
        "results": [r.to_dict() for r in results],
    }


def main():
    parser = argparse.ArgumentParser(
        description="Kernel-level grader — scores optimized kernels via Magpie."
    )
    parser.add_argument(
        "--output-dir", default=str(OUTPUT_DIR),
        help=f"Path to the output/ directory (default: {OUTPUT_DIR})",
    )
    parser.add_argument(
        "--task", default=None,
        help="Grade only tasks whose ID contains this string.",
    )
    parser.add_argument(
        "--json", action="store_true",
        help="Print full JSON summary to stdout.",
    )
    args = parser.parse_args()

    output_dir = Path(args.output_dir)
    print(f"[kernel_grader] scanning {output_dir}", file=sys.stderr)

    results = grade_all(output_dir, task_filter=args.task)
    summary = summarise(results)

    if args.json:
        print(json.dumps(summary, indent=2))
    else:
        print(f"\n{'='*50}")
        print(f"  Kernel grader results")
        print(f"{'='*50}")
        print(f"  Tasks:        {summary['tasks']}")
        print(f"  Compiled:     {summary['compiled']} / {summary['tasks']}")
        print(f"  Correct:      {summary['correct']} / {summary['tasks']}")
        print(f"  Avg speedup:  {summary['avg_speedup']:.3f}×")
        print(f"  TOTAL SCORE:  {summary['total_score']:.1f} pts")
        print(f"{'='*50}")
        for r in results:
            d = r.to_dict()
            flag = "PASS" if d["correct"] else ("COMPILE" if d["compiled"] else "FAIL")
            print(f"  {flag:7s}  {d['task_id']:30s}  "
                  f"{d['speedup']:.2f}×  {d['score']:.0f} pts"
                  + (f"  [{d['error']}]" if d["error"] else ""))


if __name__ == "__main__":
    main()
