def _detect_kernel_type(solution: Path) -> str:
    """Infer Magpie kernel type from the solution file extension and content.

    Uses "triton" for Python kernels so Magpie runs them as standalone scripts
    (checking exit code and PASS output) rather than requiring Model/get_inputs
    functions that the pytorch mode expects.
    """
    ext = solution.suffix.lower()
    if ext in (".hip", ".cu"):
        return "hip"
    return "triton"


def _run_benchmark_script(script_path: str, python_bin: str, cwd: str, timeout: int = 120) -> float:
    """Run a kernel script and measure its execution time.

    Tries multiple strategies:
    1. Run with --benchmark flag and parse BENCHMARK_MS from stdout
    2. If no BENCHMARK_MS, parse common patterns like 'avg: X.XXms' or 'X.XX ms/iter'
    3. Fall back to measuring wall-clock time of the script itself
    """
    # Strategy 1: Try --benchmark flag first
    cmd = [python_bin, script_path, "--benchmark"]
    try:
        proc = subprocess.run(cmd, capture_output=True, text=True, timeout=timeout, cwd=cwd)
        if proc.returncode == 0:
            match = re.search(r"BENCHMARK_MS:\s*([\d.]+)", proc.stdout)
            if match:
                return float(match.group(1))
    except (subprocess.TimeoutExpired, Exception):
        pass

    # Strategy 2: Run without --benchmark and try parsing output for timing
    cmd = [python_bin, script_path]
    try:
        import time as _time
        t0 = _time.monotonic()
        proc = subprocess.run(cmd, capture_output=True, text=True, timeout=timeout, cwd=cwd)
        wall_ms = (_time.monotonic() - t0) * 1000.0

        if proc.returncode != 0:
            return 0.0

        # Parse common patterns
        for pattern in [
            r"BENCHMARK_MS:\s*([\d.]+)",
            r"(?:avg|mean|median)\s*[:=]\s*([\d.]+)\s*ms",
            r"([\d.]+)\s*ms[/\s]*iter",
            r"Time[:\s]*([\d.]+)\s*ms",
        ]:
            match = re.search(pattern, proc.stdout, re.IGNORECASE)
            if match:
                return float(match.group(1))

        # Strategy 3: Use wall-clock as last resort (very rough)
        if wall_ms > 100:  # only if the script took meaningful time
            return wall_ms
    except (subprocess.TimeoutExpired, Exception):
        pass

    return 0.0


def _measure_speedup(
    cfg: dict,
    task_dir: Path,
    baseline_path: str,
    optimized_path: str,
    num_runs: int = 5,
) -> float:
    """Measure speedup with multiple runs, outlier rejection, and variance check.

    Runs each script `num_runs` times, drops top/bottom 10%, and uses
    the median of the remaining samples. Stores a ``_high_variance``
    warning in cfg if std > 20% of mean.
    """
    perf_cfg = cfg.get("performance", {})
    perf_cmd = perf_cfg.get("command", "")
    python_bin = perf_cmd.split()[0] if perf_cmd else "python3"

    def _robust_median(path: str) -> tuple[float, bool]:
        samples = []
        for _ in range(num_runs):
            ms = _run_benchmark_script(path, python_bin, str(task_dir))
            if ms > 0:
                samples.append(ms)
        if not samples:
            return 0.0, False

        samples.sort()
        n = len(samples)
        trim = max(1, n // 10)
        if n > 3:
            trimmed = samples[trim:n - trim]
        else:
            trimmed = samples

        median = trimmed[len(trimmed) // 2]
        high_var = False
        if len(trimmed) > 1:
            mean = sum(trimmed) / len(trimmed)
            if mean > 0:
                variance = sum((x - mean) ** 2 for x in trimmed) / len(trimmed)
                std = variance ** 0.5
                if std / mean > 0.20:
                    high_var = True
        return median, high_var

    baseline_ms, b_var = _robust_median(baseline_path)
    optimized_ms, o_var = _robust_median(optimized_path)

    if b_var or o_var:
        cfg["_high_variance"] = True
        print("    [grader] WARNING: high measurement variance (std > 20% of mean)",
              file=sys.stderr)

    if baseline_ms > 0 and optimized_ms > 0:
        ratio = baseline_ms / optimized_ms
        if ratio > 100:
            return 0.0
        return ratio
    return 0.0


def _apply_tampering_penalties(
    raw: dict,
    solution: Path,
    baseline_path: str,
    compiled: bool,
    correct: bool,
    speedup: float,
    speedup_cap: float = 0.0,
) -> tuple[bool, float]:
    """Detect benchmark tampering and apply correctness/speedup penalties.

    Returns (adjusted_correct, adjusted_speedup).
    """
    tampering_warnings = _detect_benchmark_tampering(solution, baseline_path) if baseline_path else []
    if not tampering_warnings:
        return correct, speedup

    for tw in tampering_warnings:
        print(f"    [grader] TAMPERING DETECTED: {tw}", file=sys.stderr)
    raw["_tampering_warnings"] = tampering_warnings
    raw["_gaming_warnings"] = tampering_warnings

    has_fake_pass = any("hardcoded PASS" in w for w in tampering_warnings)
    has_fake_benchmark = any("hardcoded BENCHMARK_MS" in w for w in tampering_warnings)

    if has_fake_pass:
        correct = False
        raw["_correctness_rejected_for_tampering"] = True
        raw["_correctness_rejected_for_gaming"] = True
        print("    [grader] Marking correct=False: solution faked PASS output",
              file=sys.stderr)
    if has_fake_benchmark and speedup > 1.0:
        cap_val = speedup_cap if speedup_cap > 0 else 1.0
        print(f"    [grader] Capping speedup from {speedup:.2f}x to {cap_val:.2f}x: "
              f"fabricated benchmark number", file=sys.stderr)
        speedup = cap_val
        raw["_speedup_capped_for_tampering"] = True
        raw["_speedup_capped_for_gaming"] = True

    return correct, speedup


def _finalize_grading_result(
    task_id: str,
    raw: dict,
    cfg: dict,
    task_dir: Path,
    baseline_path: str,
    optimized_path: str,
    solution: Path,
    compare_timeout: int,
    speedup_cap: float = 0.0,
) -> KernelResult:
    """Shared post-processing: measure speedup, detect tampering, build KernelResult.

    Used by all three correctness modes (pytorch, library_test, accordo).
    If raw already contains a pre-computed speedup (via _magpie_speedup), that
    value is used instead of re-running _measure_speedup.
    """
    compiled = raw.get("compiled", False)
    correct = raw.get("correct", False)
    speedup = raw.pop("_magpie_speedup", 0.0)

    if compiled and correct and speedup <= 0:
        speedup = _measure_speedup(cfg, task_dir, baseline_path, optimized_path)
        if speedup <= 0:
            speedup = 1.0
            raw["_no_perf_data"] = True
            print("    [grader] No performance data; assuming 1.0x", file=sys.stderr)

    correct, speedup = _apply_tampering_penalties(
        raw, solution, baseline_path, compiled, correct, speedup,
        speedup_cap=speedup_cap,
    )

    return KernelResult(
        task_id=task_id,
        compiled=compiled,
        correct=correct,
        speedup=speedup,
        raw=raw,
        error=raw.get("error", ""),
    )


